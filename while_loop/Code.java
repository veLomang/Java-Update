package while_loop;

public class Code {
	public static void main(String args[]) {
		//Counter = 0
		int counter = 0;
		
		//While the counter is less than 10, print counter-value and add 1 for every loop until counter has an int-value of 10
		while (counter < 10) {
			System.out.println(counter);
			counter++;
		}
	}
}
