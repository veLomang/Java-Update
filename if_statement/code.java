package if_statement;

public class code {
	public static void main(String args[]) {
		int test = 6;
		
		
		// | =(assigning value) | ==(if it equals) | !=(not equal to) | <,>(less than/greater than)
		// if test is equal to 9 then print 'Yes'. If not, print 'This is else ('no')'
		if (test == 9) {
			System.out.println("Yes");
		} else {
			System.out.println("This is else ('no')");
		}
	}
}
